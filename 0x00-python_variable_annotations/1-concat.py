#!/usr/bin/env python3
"""
    Concatenates two strings together.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenated string of str1 and str2.
"""


def concat(str1: str, str2: str) -> str:
    """concate strings"""
    return str1 + str2
